package arithmetic;

public class Arithmetic
{
    public static void main(String[] args)
    {
        //INTEGER DATA TYPE
        int a=14, b=5;
        int c=a/b;
        int r=a%b;
        //type cast
        float c1=(float)a/b;
        float r1=(float)a%b;
        
        System.out.println("C: "+c);
        System.out.println("R: "+r);
        System.out.println("C1: "+c1);
        System.out.println("R1: "+r1);
        
        //FLOAT DATA TYPE
    }
    
}
